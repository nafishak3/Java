package JavaBasics;

public class IncrementAndDecrementOperator {

	public static void main(String[] args) {
		
		//++
		//--
		
		int i =1;
		int j = i++;// post increment  (increments the value by i by 1 and uses the original value in for j
		
		System.out.println(i);
		System.out.println(j);
		
		System.out.println("****");
		
		int o = 10;
		int c = o++;
		System.out.println(o);
		System.out.println(c);
		
		System.out.println("****");
		
		int a = 1;
		int b = ++a; //pre increment ( increments the value a by 1 and uses the new value for b)
		
		System.out.println(a);
		System.out.println(b);
		
		System.out.println("*****");
		
		int f = 6;
		int k = ++f;
		System.out.println(f);
		System.out.println(k);
		
		
		System.out.println("*****");
		
		int m = 2;
		int n = m--;//post decrement 
		
	    System.out.println(m);
		System.out.println(n);
				
		System.out.println("****");
		int d1 = 70;
		int fc = d1--;
		System.out.println(d1);
		System.out.println(fc);
				
		System.out.println("*****");
		
		int p = 2;
		int s = --p; // pre decrement 
		System.out.println(p);
		System.out.println(s);
		
		System.out.println("****");
		
		int ss = 30;
		int as = --ss;
		System.out.println(ss);
		System.out.println(as);
		
		System.out.println("****");
		
		int k1 = 3;
		int n1 = ++k1; // pre 
		System.out.println(k1); //=4
		System.out.println(n1); //=4
		
		System.out.println("****");
		
		int l2 = 3;
		int lk = l2++; // post 
		System.out.println(l2); //= 4
		System.out.println(lk); //= 3
		
		System.out.println("****");
		
		int sk = 5;
		int lm = sk--; // post decrement
		System.out.println(sk); // =4
		System.out.println(lm); // =5
		
		System.out.println("****");
		
		int ll = 5;
		int nk = --ll; //pre
		System.out.println(ll);// = 4
		System.out.println(nk);// = 4
	
		System.out.println("****");
		
		int k11 = 4; 
		int l11 = k11--;  // pree decremnetal k11 = 3, l11= 4
		System.out.println(k11);
		System.out.println(l11);
		
		
		System.out.println();
		
				
		
	
				
		
				
		
		
		

	}

}
