package JavaBasics;


public class StringConcatenation {

	public static void main(String[] args) {
		
		// + sign is concatenation operator
		// print -- is just used to print on the console 
		// println -- is used to print a new line on the console
		
		int a = 100;
		int b = 200;
		
		String x = "Hello";
		String y = "World";
		
		double c = 12.33;
		double d = 10.33;
		
		System.out.println(a+b);
		
		System.out.println("********");
		
		System.out.println(x+y);
		
		System.out.println("********");
		
		System.out.println(a+b+x+y);
		
		System.out.println("********");
		
		
		System.out.println(x+y+a+b);
		
		System.out.println("********");
		
		
		System.out.println(x+y+(a+b));
		
		System.out.println("********");
		
		
		System.out.println(a+b+x+y+a+x+b+y);
		
		System.out.println("********");
		
		
		System.out.println(c+d);
		
		System.out.println("********");
		
		
		System.out.println(x+y+c+d);
		
		System.out.println("********");
		
		
		System.out.println("HelloWorld");
		
		System.out.println("********");
		
		
		System.out.println(a);
		
		System.out.println("********");
		
		
		System.out.println("the value of b is:" + b);
		
		System.out.println("********");
		
		
		System.out.println("the addition of a and b is:" +(a+b));
		
		System.out.print("Hello Selenium"); // print -- is just used to print on the console 
		System.out.println("Hello Testing"); // println -- is used to print a new line on the console
		

		
		
		
		
		
		
		

	}

}
